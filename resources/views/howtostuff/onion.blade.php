                        <p>
                            Наличие незашифрованного участка в конце неизбежно. Но это не значит, что TOR только вредит безопасности и его создали чтобы собрать всех бандитов в одном месте и с выходной ноды трафик стричь. Важным является то, что речь идёт именно о шифровании TOR. То есть это дополнительное шифрование, но ничего не мешает трафику быть зашифрованным протоколом общения с сервером. Для веб-сайтов например <a href="https://www.cloudflare.com/learning/ssl/what-is-https/" target="_blank">HTTPS</a> почитайте про сертификаты <a href="https://neerc.ifmo.ru/wiki/index.php?title=SSL/TLS" target="_blank">TLS и SSL</a> и есть ресурсы, которые работают по незащищенным протоколам такие как HTTP или FTP, конечно же много приложений обмениваются незащищенным трафиком. В итоге <strong>если используется TOR</strong> то обязательно надо проверять что на сайтах, в которых вводится какая-либо важная информация, есть HTTP(S) и НИКОГДА нельзя игнорировать в TOR ошибки сертификата в браузере, пока не ясна причина по которая она случилась. 
                             <br>
                             Еще пару минусов:
                             <ul>
                                 <li>
                                    Все IP адреса TOR в black lists. А это значит, что при использовании google, надо постоянно разгадывать ребусы, в последнее время доступ постоянно блокируется. Для того чтобы попасть на большиство сайтов, придётся проходить много проверок. 
                                 </li>
                                 <li>
                                     Постоянно имеющийся IP не позволяет логиниться на сайтах, где сессия привязывается к IP.
                                </li>
                                <li>
                                     При попытке входа в онлайн-банк и прочие системы где присутствует "анти-фрод" аккаунт будет моментально заблокирован. (Да и вообще, вы же не идиот чтобы установить TOR и попробовать зайти в свой акаунт типа "Ого внатуре я зашифрован") Попытка входа в онлайн банк через TOR - равносильна попытке взлома для серьезных систем защиты. 
                                </li>
                                <li>
                                    Заметное падение скорости соединения.
                                </li>
                             </ul>
                        </p>
                        <p>
                            И еще конечно же есть ньюанс. который редко где упоминается : даже в TOR браузере можно снять fingureprint. В связи с тем, что TOR не используется массово. Именно факт его применения зачастую используется как основа для отслеживания пользователя. 
                            <br>
                            <strong>
                                fingerprint - уникальный отпечаток браузера. Он генерируется на основе настроек браузера, его версии, конфигурации системы, быстродействия железа и тд.
                            </strong>
                            <br><br>
                            Снимать отпечаток точно получается не всегда, но чтобы контролировать этот процесс и поддерживать fingerprint когда нам это нужно, возможностей одного tor browser недостаточно.

                            В придачу есть Java аплеты, Adobe Flash, Adobe Shockwave, QuickTime, VBScript это технологии, с которыми может взаимодействовать браузер, но они не являются его частью. Значит, что и настройки доступа в интернет у них свои, отличные от браузера. И если страница запросила активацию какого-то разрешения, то есть вероятность, что это разрешение выйдет в интернет под реальным IP, а эта угроза безопасности.

                            На подлесок упомяну, что есть сервисы типа onion.to. Через них без тор браузера можно открыть любой тор сайт, достаточно только в конец добавить .to Но лучше никогда это не использовать, это не анонимно и ваш IP не скрывается, более того создатель сервиса может просматривать весь трафик, так как защиты тора при пользовании такими сервисами нет. И были случаи, когда провайдеры использовали эти сервисы для мошенничества.
                        </p>

